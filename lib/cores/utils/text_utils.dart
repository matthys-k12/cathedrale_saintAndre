/// Normalise Unicode NFD -> NFC pour les caracteres francais.
/// Traite les codeUnits (entiers hex) directement : aucun probleme d'encodage.
String nfc(String? text) {
  if (text == null || text.isEmpty) return '';

  const combining = <int, Map<int, int>>{
    0x0300: {
      0x61: 0xE0, 0x65: 0xE8, 0x69: 0xEC, 0x6F: 0xF2, 0x75: 0xF9,
      0x41: 0xC0, 0x45: 0xC8, 0x49: 0xCC, 0x4F: 0xD2, 0x55: 0xD9,
    },
    0x0301: {
      0x61: 0xE1, 0x65: 0xE9, 0x69: 0xED, 0x6F: 0xF3, 0x75: 0xFA,
      0x41: 0xC1, 0x45: 0xC9, 0x49: 0xCD, 0x4F: 0xD3, 0x55: 0xDA,
    },
    0x0302: {
      0x61: 0xE2, 0x65: 0xEA, 0x69: 0xEE, 0x6F: 0xF4, 0x75: 0xFB,
      0x41: 0xC2, 0x45: 0xCA, 0x49: 0xCE, 0x4F: 0xD4, 0x55: 0xDB,
    },
    0x0303: {
      0x6E: 0xF1, 0x4E: 0xD1,
    },
    0x0308: {
      0x61: 0xE4, 0x65: 0xEB, 0x69: 0xEF, 0x6F: 0xF6, 0x75: 0xFC,
      0x41: 0xC4, 0x45: 0xCB, 0x49: 0xCF, 0x4F: 0xD6, 0x55: 0xDC,
    },
    0x0327: {
      0x63: 0xE7, 0x43: 0xC7,
    },
  };

  final units = text.codeUnits;
  final result = <int>[];

  for (int i = 0; i < units.length; i++) {
    final cp = units[i];
    if (i + 1 < units.length) {
      final next = units[i + 1];
      final map = combining[next];
      if (map != null) {
        final composed = map[cp];
        if (composed != null) {
          result.add(composed);
          i++;
          continue;
        }
      }
    }
    result.add(cp);
  }

  return String.fromCharCodes(result);
}